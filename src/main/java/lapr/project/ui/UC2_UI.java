    private String[] eventList() {

        String[] list = new String[ec.getEventRegister().getEventsWhereUserIsOrganizer(u).size()];
        int cont = 0;
        for (Event e : ec.getEventRegister().getEventsWhereUserIsOrganizer(u)) {
            list[cont] = e.getTitle();
            cont++;
        }
        return list;
    }

